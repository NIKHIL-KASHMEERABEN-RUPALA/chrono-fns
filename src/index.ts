/**
 * Adds two numbers together
 * @example add(2, 3) // returns 5
 */
export function add(a: number, b: number): number {
  return a + b;
}

/**
 * Returns a greeting message
 * @example greet("World") // returns "Hello, World!"
 */
export function greet(name: string): string {
  return `Hello, ${name}!`;
}